public class Loops {
    public static void main(String[] args) {
        // for loop
       int a = 0, b = 0, c = 0;
       for (a = 0; a <= 20; a++){
           System.out.println(a);
       }


        // do while loop
        do{
            System.out.println(b);
            b++;
        }while (b<=20);




        // while loop
        while (c<=20){
            System.out.println(c);
            c++;
        }
    }
}
